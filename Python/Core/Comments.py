# Creating a Comment

#%% Example
# This is a comment.
print("Hello, World!")

#%% Example
print("Hello, World!")  # This is a comment

#%% Example
# print("Hello, World!")
print("Cheers, Mate!")


# Multi Line Comments

#%% Example
# This is a comment
# written in
# more than just one line
print("Hello, World!")

#%% Example
"""
This is a comment
written in
more than just one line
"""
print("Hello, World!")
